    /**
     * Устанавилвает цену товара
     * @return mixed
     */
    public function setProductPrice()
    {
        $data = Input::all();

        $substr = 'price-product-';

        if (strpos($data['id'], $substr) !== false && !empty($data['price'])) {
            $productId = substr($data['id'], strlen($substr));
        }

        $product = Product::find($productId);

        $product->price = $data['price'];

        $product->save();

        return $product;
    }
